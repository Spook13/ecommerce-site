// --- Coffee Section ---
/* Set the width of the side navigation to 250px and the left margin of the page content to 250px */
function openCoffeeProducts() {
    document.getElementById("CoffeeProducts").style.width = "100vw";
    document.getElementById("coffeeSection").style.display = "none";
  }
  
  /* Set the width of the side navigation to 0 and the left margin of the page content to 0 */
  function closeCoffeeProducts() {
    document.getElementById("CoffeeProducts").style.width = "0";
    document.getElementById("coffeeSection").style.display = "block";
  }


